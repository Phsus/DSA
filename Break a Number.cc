class Solution{   
public:
    int waysToBreakNumber(int N){
        // code here 
          const unsigned int M = 1000000007;

        

        long long Y=(N+2)%M;

        long long Z=(N+1)%M;

        long long ans=((Y*Z)/2)%M;

        return ans;

    

    }
};

